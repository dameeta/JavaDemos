public class Unary {
    
 public static void main(String[] args) {
    
    int x=2;
    int y=3;

//post increment => first assign and increments
    System.out.println(x++);
System.out.println(x++);
System.out.println(x);
//pre increment => first increments and assigns
++y;
System.out.println(y);
++y;
System.out.println(y);
//post decrement => first assign and decrements

//x--;
System.out.println(x--);
System.out.println(x);
//pre decrement => first decrements and assigns

}

}
