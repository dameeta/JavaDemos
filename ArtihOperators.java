public class ArtihOperators {
    

   public static void main(String[] args) {
        int var1=50;
        int var2 =10;

        System.out.println(var1+var2);
        System.out.println(var1-var2);
        System.out.println(var1*var2);
        System.out.println(var1/var2);
        System.out.println(var1%var2);
        // assignment operators
        var1+=var2;// => var1= var1+var2
        System.out.println(var1);
        var1-=var2;// => var1= var1-var2
        System.out.println(var1);
        var1*=var2;// => var1= var1*var2
        System.out.println(var1);
        var1/=var2;// => var1= var1/var2
        System.out.println(var1);
        var1%=var2;// => var1= var1%var2
        System.out.println(var1);
    
    
    

    
    
    


    }
}
