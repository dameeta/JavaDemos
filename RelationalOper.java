public class RelationalOper {
    

    public static void main(String [] args)
    {
        int x=11;int y=7;

        System.out.println(x==y);//false
        System.out.println(x<=y);//false
        System.out.println(x>=y);//true
        System.out.println(x!=y);//true
        System.out.println(x<y);//false
        System.out.println(x>y);//true

    }
}
